package datatypes;

public class datatype {
    public static void main(String args[]) {
        byte mybytenum = 120; //The byte data type can store whole numbers from -128 to 127. 
        short myshortnum = 32000; //The short data type can store whole numbers from -32768 to 32767:
        int myintnum = 12000000; //The int data type can store whole numbers from -2147483648 to 2147483647.
        long mylong = 270000000000000L; //The long data type can store whole numbers from -9223372036854775808 to 9223372036854775807. 
        float myfloatnum = 6.78f; //The float data type can store fractional numbers from 3.4e−038 to 3.4e+038. 
        double mydoublenum = 19.2060; //The double data type can store fractional numbers from 1.7e−308 to 1.7e+308. 
        System.out.println(mybytenum);
        System.out.println(myshortnum);
        System.out.println(myintnum);
        System.out.println(mylong);
        System.out.println(myfloatnum);
        System.out.println(mydoublenum);

        //Boolean:- keyword and can only take the values true or false:
        boolean isJavaFun = true;
        boolean isFishTasty = false;
        System.out.println(isJavaFun);     // Outputs true
        System.out.println(isFishTasty);   // Outputs false

        
    }
}

